export function getIntegrationsForCountry(countryCode: string): CountryIntegrations | null {
  return integrationMatrix.find((country) => country.countryCode === countryCode) || null
}

export function getAllCountries(): { code: string; name: string; currency: string }[] {
  return integrationMatrix.map((country) => ({
    code: country.countryCode,
    name: country.country,
    currency: country.currency,
  }))
}

export function getIntegrationsByCategory(countryCode: string, category: keyof CountryIntegrations): Integration[] {
  const country = getIntegrationsForCountry(countryCode)
  if (!country) return []

  const categoryData = country[category]
  return Array.isArray(categoryData) ? categoryData : []
}
